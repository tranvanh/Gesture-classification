#ifndef MultiLeapDataTypes_h
#define MultiLeapDataTypes_h

/// <summary>
/// Struct representing vector of three floats.
/// </summary>
struct MultiLeap_Vector3f {
	/// <summary>
	/// X component of the vector.
	/// </summary>
	float x;

	/// <summary>
	/// Y component of the vector.
	/// </summary>
	float y;

	/// <summary>
	/// Z component of the vector.
	/// </summary>
	float z;
};

/// <summary>
/// Quaternion representing vector of three floats.
/// </summary>
struct MultiLeap_Quaternionf {
	/// <summary>
	/// X component of the vector.
	/// </summary>
	float x;

	/// <summary>
	/// Y component of the vector.
	/// </summary>
	float y;

	/// <summary>
	/// Z component of the vector.
	/// </summary>
	float z;

	/// <summary>
	/// W component of the vector.
	/// </summary>
	float w;
};

#endif /* MultiLeapDataTypes_h */